

def sex_trans(sex):
    if sex == 0:
        return u'男'
    if sex == 1:
        return u'女'
    return u'未知'


def grade_trans(grade):
    if grade == 1:
        return u'大一'
    if grade == 2:
        return u'大二'
    if grade == 3:
        return u'大三'
    if grade == 4:
        return u'大四'
    if grade == 5:
        return u'研一'
    if grade == 6:
        return u'研二'
    if grade == 7:
        return u'研三'
    return u'未知'

